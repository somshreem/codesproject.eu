<h3> Motivation </h3>

<p> The project is expected to:

Contribute with new knowledge about technical and business solutions for a whole new typ of actor and for new system solutions that both benefit an increased flexibility in the electricity system and that in long term enable new services from local energy storages to be commercialized
Create new possibilities to both generate valuable services to the end-user, building owner and grid operator by a new actor, a local system operator, that is responsible for operations and implementing the local building grid
Contribute to lowering the barriers for new technical solutions to be tested and integrated at end-user by efficient packaging

The innovation is assessed as good through the creation of a technical system and a new local energy actor that enables new business models and increased system values
 </p>
