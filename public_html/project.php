            <h3> About the Project </h3>

           <p> The project coordinated by InnoEnergy will design, develop and test a new cloud-based battery management system (A-BMS) to enable the provision of services to end-users and energy companies. The project aims to optimize the management of distributed batteries by exploring and combining multiple flexibility services to obtain the highest financial return. In the projects demonstration phase batteries with a capacity of 10-50kwh will be installed in 3-6 apartment buildings in the city of Örebro. The provision of frequency regulation to SvK will furthermore be tested by aggregating a total of 100kWp flexibility. The project will be conducted in a time period of two years, starting December 2016

