<div class="tab-pane fade in" id="goals">

<h3> Goals </h3>

<p> -Verification of required IT infrastructure including collection of status data from batteries, control signals for
activation of charging and discharging, and data communication through cloud platform
- Verification of technical pre-qualification for the provision of FCR-N frequency regulation product as prescribed by
Verification of system under multiple scenarios and with batteries from multiple vendors including recycled batteries
An investigation of climate/social impact including the effects of battery recycling
- A detailed economical analysis with required investment, payback time (ROI) for the end-consumer. The systems investments  and operational expenses will be compared to existing services for FCR-N provision from Svk´s perspective
-A full business model for flexibility services based on the results

Where: Örebro
When: 1/dec/2016 – 31/dec/2018
Participants: Ferroamp, SvK, Sweco, Energimyndigheten
